//Convert to Float 


float powerCommand(ADC_HandleTypeDef hadc){
  HAL_ADC_Start(&hadc);
  HAL_StatusTypeDef result = HAL_ADC_PollForConversion(&hadc, 100);
  
  if (result == HAL_OK){
  uint32_t rawADCvalue = HAL_ADC_GetValue(&hadc);
  float power = (rawADCvalue * 1.0f/4095.0f);
  return power;
  }

  else {
    return -1.0f;
  }


  
}


